#include "libft.h"

int	ft_atoi(const char *str)
{
	int			res;
	const char	*s;

	res = 0;
	s = str;
	while (*s && *s == ' ')
		s++;
	str = s;
	if (*s && *s == '-')
		s++;
	while (*s && *s >= '0' && *s <= '9')
	{
		res = res * 10 + (*s - '0');
		s++;
	}
	if (res != 0 && *str == '-')
		res *= -1;
	return (res);
}
